package com.jason.property.model;

public class InputTable {

	private String name;
	private String objectType;
	private String objectID;
	private String startDegree;
	private String endDegree;
	private String price;
	private String equipmentNumber;
	private String feeType;
	private String feeStandardID;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getObjectType() {
		return objectType;
	}

	public void setObjectType(String objectType) {
		this.objectType = objectType;
	}

	public String getObjectID() {
		return objectID;
	}

	public void setObjectID(String objectID) {
		this.objectID = objectID;
	}

	public String getStartDegree() {
		return startDegree;
	}

	public void setStartDegree(String startDegree) {
		this.startDegree = startDegree;
	}

	public String getEndDegree() {
		return endDegree;
	}

	public void setEndDegree(String endDegree) {
		this.endDegree = endDegree;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getEquipmentNumber() {
		return equipmentNumber;
	}

	public void setEquipmentNumber(String equipmentNumber) {
		this.equipmentNumber = equipmentNumber;
	}

	public String getFeeType() {
		return feeType;
	}

	public void setFeeType(String feeType) {
		this.feeType = feeType;
	}

	public String getFeeStandardID() {
		return feeStandardID;
	}

	public void setFeeStandardID(String feeStandardID) {
		this.feeStandardID = feeStandardID;
	}
}
